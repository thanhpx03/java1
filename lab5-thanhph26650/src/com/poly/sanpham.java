
package com.poly;

class sanpham {
    private String ten;
    double gia;

    public sanpham() {
    }

    public sanpham(String ten, double gia) {
        this.ten = ten;
        this.gia = gia;
    }
    
    
    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public double getGia() {
        return gia;
    }

    public void setGia(double gia) {
        this.gia = gia;
    }
    
    
    
}
